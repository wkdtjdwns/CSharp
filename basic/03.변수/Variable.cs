// int : 정수 (소수점이 없는 숫자)
// -> 아무런 제약없이 사용 가능하지만 값이 허용 범위를 넘을 수는 없음
// -> 허용 범위를 넘어버리는 값을 사용할 때는 long과 같은 자료형을 사용할 수 있음.
int age = 18;

// float : 실수 (소수점이 있는 숫자)
// -> 값의 뒤에 "f"를 붙여줘야 함.
// -> 허용 범위를 넘는 값은 "double"과 같은 자료형을 사용할 수 있음.
float pi = 3.14f;

// bool : 논리 (true / false)
// -> 논리형은 값이 true와 false 밖에 없음.
// -> if문과 같은 제어문에서 많이 사용됨.
bool is_adult = true;

// string : 문자열 (우리가 아는 글자들)
// -> " " (쌍따옴표) 안에 값을 입력해야 함.
string name = "유니티";
